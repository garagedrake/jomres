class j16000delete_template_override
{

	/**
	 *
	 * Constructor
	 *
	 * Main functionality of the Minicomponent
	 *
	 *
	 *
	 */
	 
	public function __construct()
	{
		// Must be in all minicomponents. Minicomponents with templates that can contain editable text should run $this->template_touch() else just return
		$MiniComponents = castor_singleton_abstract::getInstance('mcHandler');
		if ($MiniComponents->template_touch) {
			$this->template_touchable = false;

			return;
		}

		$template_overrides = castor_singleton_abstract::getInstance('template_overrides');
		$template_name		= (string) castorGetParam($_REQUEST, 'template_name', '');
		$template_overrides->delete_override($template_name);

		castorRedirect(castorURL(CASTOR_SITEPAGE_URL_ADMIN.'&task=list_template_overrides'), '');
	}


	public function getRetVals()
	{
		return null;
	}
}

